// Convert Celsius to Fahrenheit
function convertToF(celsius) {
   let fahrenheit = celsius * (9 / 5) + 32;
   return fahrenheit;
}

// Reverse a String
function reverseString(str) {
   var revStr = "";
   for (let i = 0; i < str.length; i++) revStr += str[str.length - i - 1];
   console.log(revStr);
   return revStr;
}

